@Composable
fun GrowGuideTheme(
    // get the viewModel
    themeViewModel: ThemeViewModel = viewModel(),

    // don't follow the system dark theme
    //darkTheme: Boolean = isSystemInDarkTheme(),
    //darkTheme: Boolean = false,

    // Dynamic color is available on Android 12+
    dynamicColor: Boolean = false,
    content: @Composable () -> Unit
) {
    // Observe the theme state
    val darkTheme = themeViewModel.isDarkTheme.value
    val colorScheme = when {
        dynamicColor && Build.VERSION.SDK_INT >= Build.VERSION_CODES.S -> {
            val context = LocalContext.current
            if (darkTheme) dynamicDarkColorScheme(context) else dynamicLightColorScheme(context)
        }

        darkTheme -> darkScheme
        else -> lightScheme
    }

    val view = LocalView.current
    if (!view.isInEditMode) { // check for preview
        SideEffect {
            val window = (view.context as Activity).window
            window.statusBarColor = colorScheme.background.toArgb() // use background color
            WindowCompat.getInsetsController(window, view).isAppearanceLightStatusBars = !darkTheme // Light status bar for light theme
        }
    }

    MaterialTheme(
        colorScheme = colorScheme,
        typography = Typography,
        content = content
    )
}